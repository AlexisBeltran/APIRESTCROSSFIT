const express = require("express");
const router = express.Router();
const workoutControllers = require("../../controllers/workoutController");

/**
 * @openapi
 * /api/v1/workouts:
 *   get:
 *     tags:
 *       - Workouts
 *     parameters:
 *       - in: query
 *         name: mode
 *         schema:
 *           type: string
 *         description: The mode of a workout
 *     responses:
 *       200:
 *         description: OK
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: OK
 *                 data:
 *                   type: array 
 *                   items: 
 *                     $ref: "#/components/schemas/Workout"
 *       5XX:
 *         description: FAILED
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status: 
 *                   type: string
 *                   example: FAILED
 *                 data:
 *                   type: object
 *                   properties:
 *                     error:
 *                       type: string 
 *                       example: "Some error message"
 */

router.get("/", workoutControllers.getAllWorkouts);

router.get("/:workoutId", workoutControllers.getOneWorkout);

router.post("/", workoutControllers.createNewWorkout);

router.patch("/:workoutId", workoutControllers.updateOneWorkout); 

router.delete("/:workoutId", workoutControllers.deleteOneWorkout);

module.exports = router;